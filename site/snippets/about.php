  <p>
    BLANK PAGE STUDIO is a space for people dedicated to the advancement of innovative practices and ideas in art, architecture, design, and culture. Our mandate is to engage a broad audience throughout Calgary’s communities in generating dialogue about, and active participation in the city’s cultural sphere.
  </p>
  <p>
    Designed as a hub for local emerging creative professionals, the studio was founded in 2013 by visual artist daniel j kirk, co-created by graduate architects Ivan Ostapenko and Studio North, and also includes Market Collective and Intelligent Futures.
  </p>
  <p>
    We welcome collaborations with groups and individuals working to improve communities through thoughtful integration of art and design into the future of the city’s development.
  </p>
